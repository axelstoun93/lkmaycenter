    <strong>С чего начать работу в Личном кабинете?</strong>
<ol>
 	<li><strong>Заполните раздел Мой профиль</strong> – внесите все данные о Вашей организации. Без заполнения профиля Вы не сможете размещать вакансии. Раздел находится в верхнем правом углу.</li>
</ol>
<strong>Совет:</strong> загружайте самый большой по ширине и длине логотип. Тогда он будет смотреться выигрышнее для студентов. Длину и ширину логотипа можно посмотреть в Свойствах изображения. Для хорошего отображения картинки на экране длина и ширина должны быть больше 640 пикселей.
<ol start="2">
 	<li><strong>Разместите вакансию в разделе Мои вакансии</strong>. Раздел находится на левом поле страницы.</li>
</ol>
<strong>Совет: </strong>Обязательно заполняйте все поля, ведь так больше шансов найти именно того мастера, который соответствует Вашим требованиям и ожиданиям. Вы можете разместить неограниченное количество вакансий.
<br>
<strong>Внимание!</strong> Пока что Вы можете разместить только 5 вакансий. Старые вакансии можно редактировать или удалять, но общее количество будет равно 5.
<br>
<ol start="3">
 	<li><strong>Запишитесь на зачет или экзамен</strong> в разделе Зачеты и экзамены. Раздел находится на левом поле страницы прямо под разделом Мои вакансии.</li>
</ol>
<ul>
 	<li>После перехода в раздел Вы увидите календарь с зачетами и экзаменами.</li>
 	<li>Для записи на зачет/экзамен наведите курсор на дату на календаре, щелкните левой кнопкой мыши.</li>
 	<li>Перед Вами появится иконка с подробной информацией по зачету/экзамену. Там же высветится кнопка «Записаться». Нажмите на нее, если Вы хотите присутствовать на данном мероприятии.</li>
 	<li>Ожидайте звонка менеджера Академии, который подтвердит, что Вы записаны на экзамен/зачет.</li>
</ul>
<strong>Внимание:</strong> Вы можете записаться на несколько зачетов и экзаменов или даже на все, если Вам позволяет время.
<ol start="4">
 	<li><strong>Как я получаю отклики на вакансии?</strong></li>
</ol>
Пока что Вы не можете получать отклики на вакансии через сайт. Именно поэтому мы просим Вас вносить контакты Вашей организации. Мастера будут связываться с Вами по указанным номерам, электронным почтам и обращаться к тому человеку, которого Вы укажете в графе Контактное лицо.

<strong>Что еще можно делать в Личном кабинете?</strong>
<ol>
 	<li>В верхнем правом углу Вы видите две иконки. Вот такие:</li>
</ol>
