<strong>Иконка «Календарь» (слева)</strong> показывает количество дней до окончания действия договора. Когда все 365 дней истекут, Вы больше не сможете размещать вакансии. Для того, чтобы продолжить пользоваться Личным кабинетом, необходимо продлить договор в Академии и внести клубный взнос.

<strong>Иконка «Колокольчик» (справа)</strong> – это Ваши уведомления и подсказки. Если у Академии будут новости для Вас, то Вы узнаете о них именно через эту иконку. Если Вам придет уведомление, то рядом с иконкой будет изображена красная цифра, показывающая количество уведомлений/подсказок.

